/**
 * Отримати всі доступні модулі
 */
export function getAllModules(): InformaticsModule[] {
  return [BASE_MODULE, ...ELECTIVE_MODULES];
}

/**
 * Отримати базовий модуль
 */
export function getBaseModule(): InformaticsModule {
  return BASE_MODULE;
}

/**
 * Отримати вибіркові модулі
 */
export function getElectiveModules(): InformaticsModule[] {
  return ELECTIVE_MODULES;
}

/**
 * Підібрати модулі для річного плану (70 годин)
 * @param selectedModuleNames - назви вибраних вибіркових модулів
 */
export function selectModulesForYear(selectedModuleNames: string[]): InformaticsModule[] {
  const modules: InformaticsModule[] = [BASE_MODULE];
  
  let totalHours = BASE_MODULE.hours;
  
  // Додаємо вибрані модулі
  for (const moduleName of selectedModuleNames) {
    const module = ELECTIVE_MODULES.find(m => m.name === moduleName);
    if (module && totalHours + module.hours <= 70) {
      modules.push(module);
      totalHours += module.hours;
    }
  }
  
  return modules;
}

