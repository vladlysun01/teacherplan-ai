export function getModulesForClass(
  classNum: 10 | 11
): { theory: ArtModule[]; practice: ArtModule[] } {
  if (classNum === 10) {
    return {
      theory: GRADE_10_THEORY,
      practice: GRADE_10_PRACTICE
    };
  } else {
    return {
      theory: GRADE_11_THEORY,
      practice: GRADE_11_PRACTICE
    };
  }
}

export function getAllModules(classNum: 10 | 11): ArtModule[] {
  const modules = getModulesForClass(classNum);
  // Чергуємо теорію та практику
  const result: ArtModule[] = [];
  const maxLength = Math.max(modules.theory.length, modules.practice.length);
  
  for (let i = 0; i < maxLength; i++) {
    if (i < modules.theory.length) {
      result.push(modules.theory[i]);
    }
    if (i < modules.practice.length) {
      result.push(modules.practice[i]);
    }
  }
  
  return result;
}

export function getTotalHours(classNum: 10 | 11): { theory: number; practice: number; total: number } {
  const modules = getModulesForClass(classNum);
  
  const theoryHours = modules.theory.reduce((sum, m) => sum + m.hours, 0);
  const practiceHours = modules.practice.reduce((sum, m) => sum + m.hours, 0);
  
  return {
    theory: theoryHours,
    practice: practiceHours,
    total: theoryHours + practiceHours
  };
}
