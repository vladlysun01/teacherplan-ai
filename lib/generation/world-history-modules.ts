export function getWorldHistoryModulesByClass(classNum: string) {
  return WORLD_HISTORY_MODULES[classNum as keyof typeof WORLD_HISTORY_MODULES] || [];
}

export function getTotalHoursByClass(classNum: string): number {
  const modules = getWorldHistoryModulesByClass(classNum);
  return modules.reduce((sum, module) => sum + module.hours, 0);
}
