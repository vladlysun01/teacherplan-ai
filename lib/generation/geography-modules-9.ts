// Підрахунок годин
export function getTotalHours9(): number {
  let total = 0;
  allModules9.forEach(module => {
    module.topics.forEach(topic => {
      total += topic.hours;
    });
  });
  return total; // 49 год + 3 резерв = 52
}

export function getSelectedModules9(selectedModules: string[]): GeographyModule[] {
  return allModules9.filter(module => selectedModules.includes(module.name));
}
