// Підрахунок годин
export function getTotalHours7(): number {
  let total = 0;
  allModules7.forEach(module => {
    module.topics.forEach(topic => {
      total += topic.hours;
    });
  });
  return total; // 64 год + 6 резерв = 70
}

export function getSelectedModules7(selectedModules: string[]): GeographyModule[] {
  return allModules7.filter(module => selectedModules.includes(module.name));
}
