export function getModulesForClass(
  classNum: 10 | 11,
  level: 'профільний'
): { algebra: MathematicsModule[]; geometry: MathematicsModule[] } {
  if (classNum === 10) {
    return {
      algebra: GRADE_10_PROFILE_ALGEBRA,
      geometry: GRADE_10_PROFILE_GEOMETRY
    };
  } else {
    return {
      algebra: GRADE_11_PROFILE_ALGEBRA,
      geometry: GRADE_11_PROFILE_GEOMETRY
    };
  }
}

export function getAllModules(classNum: 10 | 11, level: 'профільний'): MathematicsModule[] {
  const modules = getModulesForClass(classNum, level);
  return [...modules.algebra, ...modules.geometry];
}

export function getTotalHours(classNum: 10 | 11, level: 'профільний'): { algebra: number; geometry: number; total: number } {
  const modules = getModulesForClass(classNum, level);
  
  const algebraHours = modules.algebra.reduce((sum, m) => sum + m.hours, 0);
  const geometryHours = modules.geometry.reduce((sum, m) => sum + m.hours, 0);
  
  return {
    algebra: algebraHours,
    geometry: geometryHours,
    total: algebraHours + geometryHours
  };
}
