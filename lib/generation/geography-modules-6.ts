// Підрахунок годин
export function getTotalHours6(): number {
  let total = 0;
  allModules6.forEach(module => {
    module.topics.forEach(topic => {
      total += topic.hours;
    });
  });
  return total; // 64 год + 6 резерв = 70
}

export function getSelectedModules6(selectedModules: string[]): GeographyModule[] {
  return allModules6.filter(module => selectedModules.includes(module.name));
}
