// Підрахунок годин
export function getTotalHours8(): number {
  let total = 0;
  allModules8.forEach(module => {
    module.topics.forEach(topic => {
      total += topic.hours;
    });
  });
  return total; // 67 год + 3 резерв = 70
}

export function getSelectedModules8(selectedModules: string[]): GeographyModule[] {
  return allModules8.filter(module => selectedModules.includes(module.name));
}
