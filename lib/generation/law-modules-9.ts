// Підрахунок годин
export function getTotalHoursLaw9(): number {
  let total = 0;
  allModulesLaw9.forEach(module => {
    module.topics.forEach(topic => {
      total += topic.hours;
    });
  });
  return total; // 31 година + 4 резерв = 35
}

// Отримання модулів для вибраних тем
export function getSelectedModulesLaw9(selectedModules: string[]): LawModule[] {
  return allModulesLaw9.filter(module => selectedModules.includes(module.name));
}
