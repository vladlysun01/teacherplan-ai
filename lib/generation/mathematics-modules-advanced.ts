export function getModulesForClass(
  classNum: 10 | 11,
  level: 'поглиблений'
): { algebra: MathematicsModule[]; geometry: MathematicsModule[] } {
  if (classNum === 10) {
    return {
      algebra: GRADE_10_ADVANCED_ALGEBRA,
      geometry: GRADE_10_ADVANCED_GEOMETRY
    };
  } else {
    return {
      algebra: GRADE_11_ADVANCED_ALGEBRA,
      geometry: GRADE_11_ADVANCED_GEOMETRY
    };
  }
}

export function getAllModules(classNum: 10 | 11, level: 'поглиблений'): MathematicsModule[] {
  const modules = getModulesForClass(classNum, level);
  return [...modules.algebra, ...modules.geometry];
}

export function getTotalHours(classNum: 10 | 11, level: 'поглиблений'): { algebra: number; geometry: number; total: number } {
  const modules = getModulesForClass(classNum, level);
  
  const algebraHours = modules.algebra.reduce((sum, m) => sum + m.hours, 0);
  const geometryHours = modules.geometry.reduce((sum, m) => sum + m.hours, 0);
  
  return {
    algebra: algebraHours,
    geometry: geometryHours,
    total: algebraHours + geometryHours
  };
}
